    public void fillTable(ArrayList<KhachHang> listKhachHang){
        dtm.setRowCount(0);
        for(KhachHang kh : listKhachHang){
            dtm.addRow(new Object[]{
                kh.getId(),
                kh.getTenKhachHang(),
                kh.getsDT(),
                kh.getEmail(),
                kh.getTrangThai() == 1 ? "Hoạt động" : "Dừng hoạt động"
            });
        }
    }
    
    public KhachHang getForm(){
         Integer id = this.txtId.getText().isBlank() ? 0 : Integer.valueOf(this.txtId.getText());
         String tenKH = this.txtTenKh.getText();
         String sdt =this.txtSDT.getText().toString();
         String email = this.txtEmail.getText();
         Integer trangThai = this.rdoHoatDong.isSelected() == true ? 1 : 0;       
        return new KhachHang(id, tenKH, sdt, email, trangThai);
    }
