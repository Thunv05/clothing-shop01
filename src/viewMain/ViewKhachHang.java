    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
        int row = this.tblKhachHang.getSelectedRow();
        String id = String.valueOf(tblKhachHang.getValueAt(row, 0));
        String tenKh = String.valueOf(tblKhachHang.getValueAt(row, 1));
        String sdt = String.valueOf(tblKhachHang.getValueAt(row, 2));
        String email = String.valueOf(tblKhachHang.getValueAt(row, 3));
        String trangThai = String.valueOf(tblKhachHang.getValueAt(row, 4));
        
        if (trangThai.trim().equalsIgnoreCase("Hoạt động")) {
            this.rdoHoatDong.setSelected(true);
        }else{
            this.rdoDungHoatDong.setSelected(true);
        }
        this.txtId.setText(id);
        this.txtTenKh.setText(tenKh);
        this.txtSDT.setText(sdt);
        this.txtEmail.setText(email);
        
    }//GEN-LAST:event_tblKhachHangMouseClicked

