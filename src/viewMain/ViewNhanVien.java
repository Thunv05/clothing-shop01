    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
       int row = this.tblNhanVien.getSelectedRow();
       String id = String.valueOf(tblNhanVien.getValueAt(row, 0));
       String ten = String.valueOf(tblNhanVien.getValueAt(row, 1));
       String maNv = String.valueOf(this.tblNhanVien.getValueAt(row, 2));
       String ngaySinh = String.valueOf(this.tblNhanVien.getValueAt(row, 3));
       String gioiTinh = String.valueOf(this.tblNhanVien.getValueAt(row, 4));
       String SDT = String.valueOf(this.tblNhanVien.getValueAt(row, 5));
       String chucVu = String.valueOf(this.tblNhanVien.getValueAt(row, 6));
       String trangThai = String.valueOf(this.tblNhanVien.getValueAt(row, 7));
       
       this.txtID.setText(id);
       this.txtTenNhanVien.setText(ten);
       this.txtMaNV.setText(maNv);
       try {
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        Date date = dateFormat.parse(ngaySinh);
        this.txtNgaySinh.setDate(date);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        if (gioiTinh.equalsIgnoreCase("Nam")) {
            this.rdoNam.setSelected(true);
        }else{
            this.rdoNu.setSelected(true);
        }
        this.txtSDT.setText(SDT);
        this.cbbChucVu.setSelectedItem(chucVu);
        if (trangThai.equalsIgnoreCase("Hoạt động")) {
            this.rdoHoatDong.setSelected(true);
        }else{
            this.rdoDungHoatDong.setSelected(true);
        }
       
    }//GEN-LAST:event_tblNhanVienMouseClicked

