    public void fillTable(ArrayList<NhanVien> listNhanVien){
        dtm.setRowCount(0);
        for(NhanVien nv : listNhanVien){
            dtm.addRow(new Object[]{
                nv.getId(),
                nv.getTen(),
                nv.getMaNv(),
                nv.getNgaySinh(),
                nv.getGioiTinh() == 1 ? "Nam" : "Nữ",
                nv.getsDt(),
                nv.getIdChucVu(), nv.getTrangThai() == 1 ? "Hoạt động" : "Dừng hoạt động"
            });
        }
    }
    
    public void fillComboBox(ArrayList<NhanVien> listNhanVien){
        cbbChucVu.removeAllItems();
        listNhanVien.forEach(s -> cbbChucVu.addItem(s.getIdChucVu()));
    }
    
    public NhanVien getForm(){
        Integer id = this.txtID.getText().isBlank() ? 0 : Integer.valueOf(this.txtID.getText());
        String ten = this.txtTenNhanVien.getText();
        String maNV = this.txtMaNV.getText();
        Date ngaySinh = this.txtNgaySinh.getDate();
        Integer gioiTinh = this.rdoNam.isSelected() == true ? 1 : 0;
        String SDT = this.txtSDT.getText();
        int selectedComboBoxChucVu = this.cbbChucVu.getSelectedIndex();
        String idChucVu = String.valueOf(chucVuRepository.getAll().get(selectedComboBoxChucVu).getId());
        Integer trangThai = this.rdoHoatDong.isSelected() == true ? 1 : 0;
        NhanVien nhanVien = new NhanVien(id, ten, maNV, ngaySinh, gioiTinh, SDT, idChucVu, trangThai);
        return nhanVien;
    }
